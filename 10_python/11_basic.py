#파이썬의 특징
#1.파이썬은 인터프리터 언어이다.(vs 컴파일 언어)
#2.파이썬은 동적 타이핑 언어(dynamic typing) 언어이다. 타입 지정을 동적으로
#3.파이썬은 들여쓰기(indentation) 중요하다. 공식기준 : space 4개(tab)
#4.파이썬은 간결하고 쉬운 언어이다.(근데싫음)


